#define dump(x) cerr << #x << " = " << x << '\n';
using ll = long long;
template<typename T, typename U>
ostream& operator << (ostream& os, const pair<T, U>& p) {
        os << p.first << " " << p.second;
        return os;
}
template<typename T>
ostream& operator << (ostream& os, const vector<T>& vec) {
	for (int i = 0; i < vec.size(); i ++) {
		os << vec[i] << (i + 1 == vec.size() ? "" : " ");
	}
	return os;
}

void solve() {
        int n, d;
        cin >> n >> d;
        int m = sqrt(d);
        bool ok = false;
        for (int i = max(0, m - 1000); i <= min((int) 1e9, m + 1000); i ++) {
                ll days = i + (d + i) / (i + 1);
                if (days <= n) {
                        ok = true;
                        break;
                }
        }
        cout << (ok ? "YES\n" : "NO\n");
}

int main() {
        cin.tie(nullptr);
        ios::sync_with_stdio(false);
        int t; cin >> t;
        // int t = 1;
        while (t --) {
                solve();
        }
        return 0;
}
