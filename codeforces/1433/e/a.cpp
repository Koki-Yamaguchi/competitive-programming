#define dump(x) cerr << #x << " = " << x << '\n';
using ll = long long;
template<typename T>
ostream& operator << (ostream& os, vector<T>& vec) {
	for (int i = 0; i < vec.size(); i ++) {
		os << vec[i] << (i + 1 == vec.size() ? "" : " ");
	}
	return os;
}

void solve() {
        int n;
        cin >> n;
        vector<ll> fact(n + 1);
        fact[0] = 1;
        rep(i, n) {
                fact[i + 1] = fact[i] * (i + 1);
        }
        ll ans = fact[n] / fact[n - n / 2] / fact[n / 2] / 2;
        ans *= fact[n / 2 - 1] * fact[n / 2 - 1];
        cout << ans << '\n';
}

int main() {
        cin.tie(nullptr);
        ios::sync_with_stdio(false);
        // int t; cin >> t;
        int t = 1;
        while (t --) {
                solve();
        }
        return 0;
}
