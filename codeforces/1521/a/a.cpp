void solve() {
        ll a, b;
        cin >> a >> b;
        if (b == 1) {
                cout << "NO\n";
                return;
        }
        cout << "YES\n";
        if (b == 2) {
                cout << a << " " << 3 * a * b - a << " " << 3 * a * b << '\n'; 
        } else {
                cout << a << " " << a * b - a << " " << a * b << '\n';
        }
}

int main() {
        cin.tie(nullptr);
        ios::sync_with_stdio(false);
        int t; cin >> t;
        // int t = 1;
        while (t --) {
                solve();
        }
        return 0;
}
